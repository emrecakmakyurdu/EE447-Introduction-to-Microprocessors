		AREA	main,	READONLY,	CODE
		THUMB
		EXTERN	CONVRT
		EXTERN	OutStr
		EXTERN	InChar
		EXPORT	__main
		
NUM		EQU		0X20000100
DEC		EQU		0X20000200	


__main
		LDR		R0,=NUM			;0X20000200 ADRESINI ATA
		MOV32	R1,#0X80000000	; NUMBER TO BE PRINTED
		STR		R1,[R0]
get		BL		InChar
		CMP		R5,#0X20		;SPACEBAR GIRDIYSE PROGRAMDAN CIK
		BEQ		LOOP
		
		LDR		R4,[R0]			;O ADRESTEKI SAYIYI R4'E ATA
		LDR		R5,=DEC			;R4'TEKI SAYIYI CONVERT EDIP ATAYACAGIMIZ ADRESI R5'E ATA
		BL		CONVRT			;CONVERT ET
		LDR		R5,=DEC			;CONVERT EDILMIS ADERSIN BASINI ATA
		BL		OutStr			;OUTPUT ET
		B		get
			
LOOP	B		LOOP
		END